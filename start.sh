rm -f /app/Data/server.properties

# Тут можно создать новые параметры по умолчанию (если твой сервер сам их не создает)
# echo "{ \"port\": 7777, \"max_players\": 10 }" > /app/Data/config.json

echo "=== [2] Запуск Playit.gg туннеля ==="
# Запускаем playit в фоне. 
# ВАЖНО: Он выдаст ссылку для привязки прямо в логи Zeabur!
playit &

echo "=== [3] Запуск .NET Сервера ==="
# Если твой сервер в виде исходного кода в репозитории:
# dotnet run 

# ЕСЛИ ТЫ СКАЧИВАЕШЬ готовый сервер откуда-то по ссылке, раскомментируй (убери #) строки ниже:
# wget "ТУТ_ССЫЛКА_НА_ТВОЙ_СЕРВЕР.zip" -O server.zip
# unzip server.zip
# dotnet ТвойСервер.dll

# (Пока оставим заглушку, чтобы контейнер не выключался, если нет файлов)
echo "Сервер запущен и работает в фоне!"

# Эта команда нужна, чтобы контейнер не закрылся после выполнения скрипта
